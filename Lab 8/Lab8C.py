import math
a = float(input("Enter the first number: "))
b = float(input("Enter the second number: "))
print(f"Ceiling: {math.ceil(a)}")
print(f"Floor: {math.floor(a)}")
print(f"Absolute value: {math.fabs(a):.3f}")
print(f"Square root: {math.sqrt(b):.3f}")
print(f"Cube root: {math.cbrt(b):.3f}")
print(f"Log base 2: {math.log(b, 2):.3f}")